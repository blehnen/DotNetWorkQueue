using System.Collections.Generic;

namespace DotNetWorkQueue
{
    /// <summary>
    /// Defines a connection to a queue
    /// </summary>
    /// <remarks>Implementations should correctly implement <see cref="Equals"/> and <see cref="GetHashCode"/></remarks>
    public interface IConnectionInformation
    {
        /// <summary>
        /// Gets the name of the queue.
        /// </summary>
        /// <value>
        /// The name of the queue.
        /// </value>
        string QueueName { get; }
        /// <summary>
        /// Gets the connection string.
        /// </summary>
        /// <value>
        /// The connection string.
        /// </value>
        string ConnectionString { get; }

        /// <summary>
        /// Gets the additional connection settings.
        /// </summary>
        IReadOnlyDictionary<string, string> AdditionalConnectionSettings { get; }

        /// <summary>
        /// Gets the server.
        /// </summary>
        /// <value>
        /// The server.
        /// </value>
        /// <remarks>The server display name. Used for display, no processing</remarks>
        string Server { get; }

        /// <summary>
        /// Gets the container.
        /// </summary>
        /// <value>
        /// The container.
        /// </value>
        /// <remarks>The name of the container for the queue</remarks>
        string Container { get; }

        /// <summary>
        /// Returns a <see cref="string" /> that represents this instance.
        /// </summary>
        /// <returns>
        /// A <see cref="string" /> that represents this instance.
        /// </returns>
        string ToString();

        /// <summary>
        /// Determines whether the specified <see cref="object" />, is equal to this instance.
        /// </summary>
        /// <param name="obj">The <see cref="object" /> to compare with this instance.</param>
        /// <returns>
        ///   <c>true</c> if the specified <see cref="object" /> is equal to this instance; otherwise, <c>false</c>.
        /// </returns>
        bool Equals(object obj);

        /// <summary>
        /// Returns a hash code for this instance.
        /// </summary>
        /// <returns>
        /// A hash code for this instance, suitable for use in hashing algorithms and data structures like a hash table. 
        /// </returns>
        int GetHashCode();

        /// <summary>
        /// Returns a deep copy of this instance.
        /// </summary>
        /// <returns></returns>
        IConnectionInformation Clone();
    }
}
